package CKJY;

public class UserDTO {
	int user_uid;
	String user_id;
	String user_pw;
	String user_name;
	String user_gender;
	String user_jumin;
	int user_age;
	int user_authorize;
	String user_email; 
	String user_cardnum;
	int ticketnum;
	
	public UserDTO() {
		
	}  
	
	
	
	public UserDTO(int user_uid, String user_id, String user_pw, String user_name, String user_gender,
			String user_jumin, int user_age, int user_authorize, String user_email, String user_cardnum) {
		super();
		this.user_uid = user_uid;
		this.user_id = user_id;
		this.user_pw = user_pw;
		this.user_name = user_name;
		this.user_gender = user_gender;
		this.user_jumin = user_jumin;
		this.user_age = user_age;
		this.user_authorize = user_authorize;
		this.user_email = user_email;
		this.user_cardnum = user_cardnum;
	}



	public int getUser_uid() {
		return user_uid;
	}
	public void setUser_uid(int user_uid) {
		this.user_uid = user_uid;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getUser_pw() {
		return user_pw;
	}
	public void setUser_pw(String user_pw) {
		this.user_pw = user_pw;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public String getUser_gender() {
		return user_gender;
	}
	public void setUser_gender(String user_gender) {
		this.user_gender = user_gender;
	}
	public String getUser_jumin() {
		return user_jumin;
	}
	public void setUser_jumin(String user_jumin) {
		this.user_jumin = user_jumin;
	}
	public int getUser_age() {
		return user_age;
	}
	public void setUser_age(int user_age) {
		this.user_age = user_age;
	}
	public int getTicketnum() {
		return ticketnum;
	}



	public void setTicketnum(int ticketnum) {
		this.ticketnum = ticketnum;
	}



	public int getUser_authorize() {
		return user_authorize;
	}
	public void setUser_authorize(int user_authorize) {
		this.user_authorize = user_authorize;
	}
	public String getUser_email() {
		return user_email;
	}
	public void setUser_email(String user_email) {
		this.user_email = user_email;
	}
	public String getUser_cardnum() {
		return user_cardnum;
	}
	public void setUser_cardnum(String user_cardnum) {
		this.user_cardnum = user_cardnum;
	}

}
